int	ft_int_len(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
	{
		len += 2;
		n = 147483648;
	}
	if (n < 0)
	{
		len++;
		n *= -1;
	}
	while (n >= 10)
	{
		n /= 10;
		len++;
	}
	if (n < 10)
		len++;
	return (len);
}
