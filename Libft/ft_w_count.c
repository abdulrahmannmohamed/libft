int		ft_w_count(const char *s, char c)
{
	int		count;

	count = 0;
	if (!s || !c)
		return (0);
	while (*s)
	{
		if (*s == c)
			s++;
		else
		{
			count++;
			while (*s && *s != c)
				s++;
		}
	}
	return (count);
}
